public class Lingkaran {
    // Metode untuk menghitung luas lingkaran
    public double hitungLuas(double r) {
        final double PI = 3.141592;
        double luas = PI * r * r;
        return luas; // Mengembalikan hasil perhitungan
    }
    
}
